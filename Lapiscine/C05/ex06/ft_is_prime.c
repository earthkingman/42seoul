int	ft_is_prime(int nb)
{
	int i;
	int nbr;

	if (nb <= 1)
		return (0);
	nbr = nb / 2;
	i = 2;
	while (i <= nbr)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
