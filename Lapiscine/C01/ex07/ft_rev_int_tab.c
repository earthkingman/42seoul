void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int j;
	int temp[1024];

	i = 0;
	j = 0;
	while (i < size)
	{
		temp[i] = tab[i];
		i++;
	}
	while (j < size)
	{
		tab[j] = temp[size - 1 - j];
		j++;
	}
}
