char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int i;
	int nn;

	nn = (int)n;
	i = 0;
	while (i < nn && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	while (i < nn)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}
