int		ft_str_is_numeric(char *str)
{
	int		i;
	char	ii;

	i = 0;
	while (str[i] != '\0')
	{
		ii = str[i];
		if (!(ii >= '0' && ii <= '9'))
			return (0);
		i++;
	}
	return (1);
}
