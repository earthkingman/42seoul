#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*a;

	i = 0;
	while (src[i] != '\0')
		i++;
	a = (char *)malloc(sizeof(char) * i + 1);
	i = 0;
	while (src[i] != '\0')
	{
		a[i] = src[i];
		i++;
	}
	a[i] = '\0';
	return (a);
}
