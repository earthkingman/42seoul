#include "philo.h"

int	ft_atoi(const char *s)
{
	long	res;
	int		minus;

	res = 0;
	minus = 1;
	while (*s == ' ' || (*s >= 9 && *s <= 13))
		s++;
	if (*s == '+' || *s == '-')
		minus = *s++ == '-' ? -1 : 1;
	while (*s >= '0' && *s <= '9')
	{
		res = res * 10 + (*s - '0');
		if (res < 0)
			return (minus == 1 ? -1 : 0);
		s++;
	}
	return ((int)res * minus);
}
