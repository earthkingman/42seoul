#include "libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int sign;
	int answer;

	i = 0;
	sign = 1;
	answer = 0;
	while (str[i] != 0 && (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13)))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = sign * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		answer = answer * 10 + (str[i] - 48);
		i++;
	}
	return (sign * answer);
}
